package EXERCICIOS_POLIMORFISMO_EX2;

public class Animal {
		// Atributos gerais
	
		private String nome;
		private int idade;
		private String som;
		private String fazAlgo;
		
		
		public String getFazAlgo() {
			return fazAlgo;
		}
		public void setFazAlgo(String fazAlgo) {
			this.fazAlgo = fazAlgo;
		}
		public String getNome() {
			return nome;
		}
		public void setNome(String nome) {
			this.nome = nome;
		}
		public int getIdade() {
			return idade;
		}
		public void setIdade(int idade) {
			this.idade = idade;
		}
		public String getSom() {
			return som;
		}
		public void setSom(String som) {
			this.som = som;
		}
		
	
		}
		
		
		
		

	